// Switch 

let x = 1;

switch (x) {
    case 0:         
    console.log("O valor de X é 0");    
    break; 
    case 1:         
        console.log("O valor de X é 1");    
        break;   
    case 2:         
        console.log("O valor de X é 2");    
        break;   
    case 3:         
        console.log("O valor de X é 3");    
        break;   
    case 4:         
        console.log("O valor de X é 4");    
        break;   
    default:        
        console.log("O valor de X é default");    
        break;   
}